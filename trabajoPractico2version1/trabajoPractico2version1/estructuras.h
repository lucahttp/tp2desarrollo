{
	float precio;
	Marcax marca;
	SaborBebida sabor;
};
struct Panceta
{
	float precio;
	int grasocidad;
	int pCoccion;
};
struct Cheddar
{
	float precio;
	int grasocidad;
};
struct Pan
{
	float precio;
	bool integral;
};

